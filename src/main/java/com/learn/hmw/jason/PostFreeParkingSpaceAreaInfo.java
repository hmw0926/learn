package com.learn.hmw.jason;

public class PostFreeParkingSpaceAreaInfo {
	
    private Integer areaCode;
    
    private String areaName;

    private Integer areaFreeSpaceNum;

	public Integer getAreaCode() {
		return areaCode;
	}

	public void setAreaCode(Integer areaCode) {
		this.areaCode = areaCode;
	}

	public String getAreaName() {
		return areaName;
	}

	public void setAreaName(String areaName) {
		this.areaName = areaName;
	}

	public Integer getAreaFreeSpaceNum() {
		return areaFreeSpaceNum;
	}

	public void setAreaFreeSpaceNum(Integer areaFreeSpaceNum) {
		this.areaFreeSpaceNum = areaFreeSpaceNum;
	}

}
